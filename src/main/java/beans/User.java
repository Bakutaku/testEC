package beans;

/**
 * ユーザデータ
 */
public class User {
	private String userid;	// ユーザID
	private String passwd;	// パスワード
	
	
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	
	
}
